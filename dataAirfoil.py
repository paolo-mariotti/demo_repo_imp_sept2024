import numpy as np
import matplotlib.pyplot as plt
from scipy.interpolate import interp1d
# Function to plot airfoil shape from dictionary
def plot_airfoil(airfoil_name):
    # Extract airfoil data from the dictionary
    airfoil_data = airfoils_data[airfoil_name]
    
    # Plot airfoil shape
    plt.plot(airfoil_data[:, 0], airfoil_data[:, 1], label=airfoil_name)
    
    # Label the axes
    plt.xlabel('x')
    plt.ylabel('y')
# Function to interpolate airfoil points
def interpolate_airfoil(points, num_points=80):
    x, y = zip(*points)
    t_values = np.linspace(0, 1, len(points))
    interp_x = interp1d(t_values, x, kind='linear')
    interp_y = interp1d(t_values, y, kind='linear')
    new_t_values = np.linspace(0, 1, num_points)
    interpolated_points = np.array(list(zip(interp_x(new_t_values), interp_y(new_t_values))))
    return interpolated_points

